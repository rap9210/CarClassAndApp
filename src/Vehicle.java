public class Vehicle {


    private int doors;
    private String color;
    private String hasSunroof;
    private String energyConsumed;

    public String getEnergyConsumed() {
        return energyConsumed;
    }

    public void setEnergyConsumed(String energyConsumed) {
        this.energyConsumed = energyConsumed;
    }

    public int getDoors() {
        return doors;
    }

    public void setDoors(int doors) {
        this.doors = doors;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getHasSunroof() {
        return hasSunroof;
    }

    public void setHasSunroof(String hasSunroof) {
        this.hasSunroof = hasSunroof;
    }
}
